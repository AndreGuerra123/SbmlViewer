<template src="./tool-bar.html"></template>
<style lang="scss" src="./tool-bar.scss"></style>
<script>
import { readXmlUpload } from '../../utilites/readXmlUpload'
import $ from 'jquery'

export default {
  name: 'ToolBar',
  props: [
    'options'
  ],
  data () {
    return {
      isSpin: false,
      fileName: 'No file choosen',
      optionsDisplay: {
        'useNames': {
          'title': ' \'names\' as identifiers',
          'notes': 'Use \'names\' attribute instead of \'id\' attribute. Turn on the option for models with very long or uninformative ids'
        },
        'correctMathml': {
          'title': ' correctons to MathML',
          'notes': 'Make some correction for MathML generated by SimBiology. Some of SimBiology models generate wrong MathML. Try this option if something looks wrong in equations.'
        },
        'equationsOff': {
          'title': ' Turn off equations',
          'notes': 'Use this option if you are not interested in math. It reduces time of transformation.'
        }
      },
      waysDisplayPage: [
        'sbml2table',
        'sbml2math'
      ],
      xslt: 'sbml2table',
      file: null,
      fileContent: null
    }
  },
  mounted () {
    this.$root.$on('startSpin', () => {
      this.isSpin = true
    })
    this.$root.$on('stopSpin', () => {
      this.isSpin = false
    })
    this.dragNdropInit()
    this.url = this.checkGetUploadReq()
    if (this.url) {
      this.uploadFileByUrl()
    }
  },
  methods: {
    refresh: function () {
      if (this.url) {
        this.uploadFileByUrl(true)
      }
      if (this.file) {
        this.uploadFileFromComputer(true)
      }
    },
    onChooseFile: function (e) {
      e.preventDefault()
      if (document.getElementById('file').files[0]) {
        this.file = document.getElementById('file').files[0]
        this.url = null
        this.uploadFileFromComputer()
      }
    },
    uploadFileFromComputer: function (isRefresh = false) {
      this.updateFileName(this.file.name)
      readXmlUpload(this.file, (err, result) => {
        if (err) throw err
        this.fileContent = result
        this.$emit('onLoadFile', result, isRefresh)
      })
    },
    updateFileName: function (name) {
      this.fileName = name
      $('title').html(name)
    },
    changeOption: function (optName) {
      this.options[optName] = !this.options[optName]
    },
    onSelectXslt: function () {
      this.options.transform = this.xslt
      this.$emit('selectedXslt', this.xslt, this.fileContent)
    },
    checkGetUploadReq: function () {
      let parameters = window.location.search.substring(1).split('&')
      if (parameters.length !== 0) {
        return parameters[0]
      } else {
        return null
      }
    },
    uploadFileByUrl: function (isRefresh = false) {
      this.file = null
      this.updateFileName(this.url.match(/[_-\w]+.xml/)[0])
      $.ajax({
        url: this.url,
        success: (result) => {
          let fileContent = result
          this.fileContent = fileContent
          this.$emit('onLoadFile', fileContent, isRefresh)
        }
      })
    },
    dragNdropInit: function () {
      $(document).on('dragover', function (e) {
        e.preventDefault()
      })
      $(document).on('dragstart', function (e) {
        e.preventDefault()
      })
      $(document).on('drop', (e) => {
        e.preventDefault()
        this.file = e.originalEvent.dataTransfer.files[0]
        this.uploadFileFromComputer()
      })
    }
  }
}
</script>
